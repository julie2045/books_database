--creating tables for book info, if I have read the book or not, if it has a movie, different library branches, and if the book is available at a certain library 
CREATE TABLE book_profile  
(
  id int primary key generated by default as identity,
  book_title varchar, 
  author varchar, 
  genre varchar, 
  public_rating float
 );
 
CREATE TABLE reading_profile
(
  book_id int,
  status varchar 
 );
  
CREATE TABLE movie_adaptation 
(
 book_id int, 
 Duration_min int,
 streaming_platform varchar
 );

CREATE TABLE library_branches
(
  id int primary key generated by default as identity,
  branch_name varchar
 );
 
CREATE TABLE library_availability
( 
  book_id int, 
  branch_id int

 );
  
 
